                <div class="h3banner">
                	<div class="bannerChild">
               			<h3> Component Requirements </h3>
                	</div>
                </div>
                
                <p class="componentDetails"><b> Game Pieces </b> - Pieces must be actual pieces - no paper tokens or counters. Pieces borrowed from other games are 
                acceptable. Pieces must be distinguishable so that players will be able to tell what they are supposed to represent. (example: 
                different color pawns, or labels placed on poker chips.)
               	</p>
                
                <p class="componentDetails"><b> Cards </b> - Cards must be able to be shuffled. Cards on plain paper can be sleeved with card grade backing to accomplish 
                this. Hand drawn cards will be accepted on a case by case basis, however it is preferred that cards be printed. Hand drawn 
                corrections to cards are acceptable. Some ideas on how to get them printed and feeling like cards:
                <ul> 
                <li> Print them out on thick card stock or business card stock and cut them out</li>
                <li> Print them out on regular paper, cut them out and put them in a card sleeve backed by card stock (magic cards work really well) </li>
                <li> Get them professionally printed (don't spend too much on this, your game designs will constantly change)</li>
                </ul>
				</p>
                
                <p class="componentDetails"><b> Game Boards</b> - If you are home printing your board it must be supported by a cardboard backing to give it a board game 
                touch. A game board that is just a piece of paper will not be accepted. This rule also applies to any pieces that represent a game 
                board, such as tiles. Some methods for accomplishing this:
                <ul> 
                <li> Print the board design on paper and mount/paste it to either cardboard, posterboard or an old game board.</li>
                <li> Print the board design and laminate it (this also protects it from damage)</li>
                <li> Get it professionally printed (don't spend too much on this, your game designs will constantly change)</li>
                </ul>
                </p>
				
				<p class="componentDetails"><b> Game Mats</b> - Home printing game mats should be protected by plastic. Using a page protector is a good way to do this. 
				This will protect the mat from spills as well as provide a professional touch. 
               	</p>
               	
               	<p class="componentDetails"><b> Artwork</b> - Artwork must be applied to the board/cards/tiles/etc. if appropriate. Blank boards/cards will not be 
                accepted, unless the spaces are intentionally left blank as part of a game mechanic. Stock imaging is OK as long as it matches 
                your theme and not every card has the same image (i.e. a card representing a plant should not have the same image as a card 
                representing a bird). 
				</p>
                
                <p>
                <br>There may be types of pieces that have not been covered. In these cases it is up to the event organizer to determine their 
                acceptability. As long as those pieces meet the same standards of quality listed above (or better), the game will be accepted. 
				Please also note that there are inexpensive ways of applying these standards to your game. If you need suggestions or ideas 
				please ask a fellow designer or an event organizer for more assistance. 
				</p>
                
				<p>Even if you do not feel your game meets these requirements, we encourage you to present your game for submission 
				anyway. You may be surprised by the results, and at the very least it lets us know you are interested in participating in the event.
				</p>
				
				<p>Thank you and KOGO! (Keep On Gaming On!)               
